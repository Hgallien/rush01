#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t			x;

	x = 0;
	if (src == NULL && dst == NULL)
		return (dst);
	if (dst < src)
	{
		while (x < len)
		{
			((unsigned char *)dst)[x] = ((unsigned char *)src)[x];
			x++;
		}
	}
	else
	{
		while (len > 0)
		{
			((unsigned char *)dst)[len - 1] = ((unsigned char *)src)[len - 1];
			len--;
		}
	}
	return (dst);
}
