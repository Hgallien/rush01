#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	l;
	char		c[1];

	l = n;
	if (l < 0)
	{
		write(fd, "-", 1);
		l = l * -1;
	}
	if (l / 10 > 0)
		ft_putnbr_fd(l / 10, fd);
	c[0] = '0' + l % 10;
	write(fd, c, 1);
}
